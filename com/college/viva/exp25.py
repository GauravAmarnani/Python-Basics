# WAP where a function returns whether the number is even or odd.

def evenOrOdd(number):
    if number % 2 == 0:
        print(number, " is even.")
    else:
        print(number, " is odd.")


userInput = int(input("Enter a Number: "))
evenOrOdd(userInput)

