# WAP to swap value of two variables.

num1 = int(input("Enter Number 1: "))
num2 = int(input("Enter Number 2: "))

print("Before Swapping: ")
print("Number 1: ", num1, " and Number 2: ", num2)

temp = num1
num1 = num2
num2 = temp

print("After Swapping: ")
print("Number 1: ", num1, " and Number 2: ", num2)

