# WAP to perform following operations on Set: Union(|), Difference(-), Intersection(&).

setName1 = {1, 2, 3, 4, 5}
setName2 = {4, 5, 6, 7, 8}
print("Union of set ", setName1, " and ", setName2, " is:\n", setName1.union(setName2))
print("\nDifference of set ", setName1, " and ", setName2, " is:\n", setName1.difference(setName2))
print("\nDifference of set ", setName2, " and ", setName1, " is:\n", setName2.difference(setName1))
print("\nIntersection of set ", setName1, " and ", setName2, " is:\n", setName1.intersection(setName2))

