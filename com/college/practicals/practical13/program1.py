# Q. Create a Matrix in Python and add, subtract, multiply, divide them.

import numpy

array1 = numpy.array([[1, 2], [3, 4]])
array2 = numpy.array([[1, 2], [3, 4]])

print("Addition:\n", numpy.add(array1, array2))
print("Subtraction:\n", numpy.subtract(array1, array2))
print("Multiplication:\n", numpy.multiply(array1, array2))
print("Division:\n", numpy.divide(array1, array2))
