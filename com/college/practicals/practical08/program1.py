# Q. Create a Set, add members in a Set and remove one item from Set.

# Creating Set:
set1 = {1, 2, 3, 4, 5}

# Adding an Element in Set:
set1.add(6)

# Adding multiple Elements in Set:
set1.update([7, 8, 9])

# Removing an Element in Set using remove():
set1.remove(9)

# Removing an Element in Set using discard():
set1.discard(8)

print(set1)
