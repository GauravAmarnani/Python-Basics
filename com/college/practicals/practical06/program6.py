# Q. Common Items from 2 Lists.

list1 = []
numberOfItems = int(input("Enter Number of Items you want to add in List 1 : "))

while numberOfItems > 0:
    listItem = int(input("Item : "))
    list1.append(listItem)
    numberOfItems -= 1

